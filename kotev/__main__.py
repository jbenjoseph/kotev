from fire import Fire

ALPHABET = {
    # Standard letters
    0x5D0: 0x10900,
    0x5D1: 0x10901,
    0x5D2: 0x10902,
    0x5D3: 0x10903,
    0x5D4: 0x10904,
    0x5D5: 0x10905,
    0x5D6: 0x10906,
    0x5D7: 0x10907,
    0x5D8: 0x10908,
    0x5D9: 0x10909,
    0x5DB: 0x1090A,
    0x5DC: 0x1090B,
    0x5DE: 0x1090C,
    0x5E0: 0x1090D,
    0x5E1: 0x1090E,
    0x5E2: 0x1090F,
    0x5E4: 0x10910,
    0x5E6: 0x10911,
    0x5E7: 0x10912,
    0x5E8: 0x10913,
    0x5E9: 0x10914,
    0x5EA: 0x10915,

    # End letters
    0x5DA: 0x1090A,
    0x5DD: 0x1090C,
    0x5DF: 0x1090D,
    0x5E3: 0x10910,
    0x5E5: 0x10911
}

def assyrian_to_phoenician(prompt: str) -> str:
    res = []
    for i in prompt:
        code = ALPHABET.get(ord(i))
        if code:
            res.append(chr(code))
        else:
            res.append(i)
    return ''.join(res)

class Controller:
    def to_phoenician(self, prompt: str):
        print(assyrian_to_phoenician(prompt))

def main():
    Fire(Controller)
